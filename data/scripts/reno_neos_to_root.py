def buildHisto1D(spectrum, covariance, name1, title1, name2, title2):
    binEdges = []
    contents = []
    for l in spectrum.split('\n'):
        l = l.strip()
        if len(l) == 0: continue

        binLo, binCen, binHi, content = [float(x) for x in l.split()]
        binEdges.append(binLo)
        binEdges.append(binHi)
        contents.append(content)
    binEdges = sorted(list(set(binEdges)))
    binEdges = np.array(binEdges)

    cov = []
    for c in covariance.split():
        c = float(c)
        cov.append(c)
    cov = np.array(cov)
    cov = cov.reshape(len(binEdges)-1, len(binEdges)-1)

    h1 = ROOT.TH1D(name1, title1, len(binEdges)-1, binEdges)
    for i in range(len(contents)):
        h1.SetBinContent(i+1, contents[i])
        h1.SetBinError(i+1, np.sqrt(cov[i,i]))

    h2 = ROOT.TH2D(name2, title2, len(binEdges)-1, binEdges, len(binEdges)-1, binEdges)
    for i in range(len(contents)):
        for j in range(len(contents)):
            h2.SetBinContent(i+1, j+1, cov[i,j])
  
    return h1, h2

